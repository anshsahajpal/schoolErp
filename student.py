class Student:

    def __init__(self, first_name, last_name, date_of_birth, standard, parent_name, parent_contact, student_contact,
                 enrolment_number, admission_date):
        self.first_name = first_name
        self.last_name = last_name
        self.date_of_birth = date_of_birth
        self.standard = standard
        self.parent_name = parent_name
        self.parent_contact = parent_contact
        self.student_contact = student_contact
        self.enrolment_number = enrolment_number
        self.admission_date = admission_date

